//While loop
console.log("Print all odd numbers between 300 & 333")
var num=300;
while(num<=333){
	if(num%2 !== 0){
		console.log(num);
	}
	num++;
}
//For loop
console.log("Print all odd numbers between 300 & 333")
var num=333;
for(i=300;i<=num;i++){
	if(i%2 !== 0){
		console.log(i);
	}
}